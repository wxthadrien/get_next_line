#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	a;
	int		i;

	i = 0;
	a = c;
	while (s[i] != '\0')
		i++;
	while (i >= 0)
	{
		if (s[i] == a)
			return ((void*)(&s[i]));
		i--;
	}
	return (NULL);
}
